    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String p = password.getText();
        for (int i = 0; i < p.length(); i++)
        {
            if (p.charAt(i) == '\'')
            {
                p = p.substring(0, i) + "''" + p.substring(i + 1, p.length()); //in sql, to escape ', just double them. '' -> '
                i++; //have to increase i by 2 instead of 1 when ' is found, otherwise will cause an infinite loop
            }
        }
        try
        {
            Connection conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","system","Horsey3210.");
            String sql1 = "UPDATE CUSTOMER SET PASSWORD = '" + p + "' WHERE USERNAME = '" + un + "'";
            PreparedStatement pst = conn.prepareStatement(sql1);
            pst.executeQuery();
            dispose();
            JOptionPane.showMessageDialog(null,"Password Updated.\n");
        }
        catch(Exception e){System.out.println(e);}
    }//GEN-LAST:event_jButton1ActionPerformed

